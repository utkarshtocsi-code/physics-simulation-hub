// ── Helper functions ──────────────────────────────────────────────────────────

export const getByBranch = (branchId) =>
  simulations.filter((s) => s.branch === branchId);

export const getBranchInfo = (branchId) =>
  branches.find((b) => b.id === branchId);

export const searchSims = (query) => {
  const q = query.toLowerCase();
  return simulations.filter(
    (s) =>
      s.title.toLowerCase().includes(q) ||
      s.description.toLowerCase().includes(q) ||
      s.tags.some((t) => t.toLowerCase().includes(q))
  );
};

export const getByDifficulty = (level) =>
  simulations.filter((s) => s.difficulty === level);

export const getBySource = (source) =>
  simulations.filter((s) => s.source === source);

export default simulations;
